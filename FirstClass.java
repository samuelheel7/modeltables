
public class FirstClass {

	// 7th table

	static void table7() {

		int n = 20;
		for (int i = 1; i <= n; i++) {

			System.out.println(i + "x" + "7" + "=" + i*7);
		}

	}

	static void table8() {

		int n = 20;
		for (int i = 1; i <= n; i++) {

			System.out.println(i + "x" + "8" + "=" + i*8);
		}

	}

	public static void main(String[] args) {
		
		FirstClass.table7();
		FirstClass.table8();
		
	}
	
}
